package com.musical.breadway.dto;

public class MarketerDTO {
	private Integer marketer_UID;
	private String marketer_ID;
	private String marketer_Password;
	private String marketer_Name;
	private String marketer_Email;
	private String marketer_Phone;
	private String authKey;
	private Integer marketer_Status;
	
	
	public Integer getMarketer_UID() {
		return marketer_UID;
	}
	public void setMarketer_UID(Integer marketer_UID) {
		this.marketer_UID = marketer_UID;
	}
	public String getMarketer_ID() {
		return marketer_ID;
	}
	public void setMarketer_ID(String marketer_ID) {
		this.marketer_ID = marketer_ID;
	}
	public String getMarketer_Password() {
		return marketer_Password;
	}
	public void setMarketer_Password(String marketer_Password) {
		this.marketer_Password = marketer_Password;
	}
	public String getMarketer_Name() {
		return marketer_Name;
	}
	public void setMarketer_Name(String marketer_Name) {
		this.marketer_Name = marketer_Name;
	}
	public String getMarketer_Email() {
		return marketer_Email;
	}
	public void setMarketer_Email(String marketer_Email) {
		this.marketer_Email = marketer_Email;
	}
	public String getMarketer_Phone() {
		return marketer_Phone;
	}
	public void setMarketer_Phone(String marketer_Phone) {
		this.marketer_Phone = marketer_Phone;
	}
	public String getAuthKey() {
		return authKey;
	}
	public void setAuthKey(String authKey) {
		this.authKey = authKey;
	}
	public Integer getMarketer_Status() {
		return marketer_Status;
	}
	public void setMarketer_Status(Integer marketer_Status) {
		this.marketer_Status = marketer_Status;
	}
	
	
}
