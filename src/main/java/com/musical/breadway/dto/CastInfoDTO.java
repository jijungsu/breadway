package com.musical.breadway.dto;

public class CastInfoDTO {
	private Integer cast_ID;
	private Integer show_ID;
	private String cast_Part;
	private String cast_Name;
	private String cast_PhotoName;
	
	
	public Integer getCast_ID() {
		return cast_ID;
	}
	public void setCast_ID(Integer cast_ID) {
		this.cast_ID = cast_ID;
	}
	public Integer getShow_ID() {
		return show_ID;
	}
	public void setShow_ID(Integer show_ID) {
		this.show_ID = show_ID;
	}
	public String getCast_Part() {
		return cast_Part;
	}
	public void setCast_Part(String cast_Part) {
		this.cast_Part = cast_Part;
	}
	public String getCast_Name() {
		return cast_Name;
	}
	public void setCast_Name(String cast_Name) {
		this.cast_Name = cast_Name;
	}
	public String getCast_PhotoName() {
		return cast_PhotoName;
	}
	public void setCast_PhotoName(String cast_PhotoName) {
		this.cast_PhotoName = cast_PhotoName;
	}
	
}
