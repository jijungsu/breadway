package com.musical.breadway.dto;

public class TheatreDTO {

	private Integer theatre_ID;
	private String theatre_Name;
	private String theatre_Address;
	private Integer theatre_AreaCode;
	private String theatre_webAddr;
	
	
	public Integer getTheatre_ID() {
		return theatre_ID;
	}
	public void setTheatre_ID(Integer theatre_ID) {
		this.theatre_ID = theatre_ID;
	}
	public String getTheatre_Name() {
		return theatre_Name;
	}
	public void setTheatre_Name(String theatre_Name) {
		this.theatre_Name = theatre_Name;
	}
	public String getTheatre_Address() {
		return theatre_Address;
	}
	public void setTheatre_Address(String theatre_Address) {
		this.theatre_Address = theatre_Address;
	}
	public Integer getTheatre_AreaCode() {
		return theatre_AreaCode;
	}
	public void setTheatre_AreaCode(Integer theatre_AreaCode) {
		this.theatre_AreaCode = theatre_AreaCode;
	}
	public String getTheatre_webAddr() {
		return theatre_webAddr;
	}
	public void setTheatre_webAddr(String theatre_webAddr) {
		this.theatre_webAddr = theatre_webAddr;
	}
	
	
	
}
